maxColourDistance = np.sqrt(3 * 255**2)


def compareFeatures(obj1, obj2):
    print("COMPARING", obj1["name"], obj2["name"])
    labelcos = cosineSimilarity(obj1["label"], obj2["label"])
    labeldist = distance(obj1["label"], obj2["label"])
    print("label values", labelcos, labeldist)

    objectcos = cosineSimilarity(obj1["object"], obj2["object"])
    objectdist = distance(obj1["object"], obj2["object"])
    print("object values", objectcos, objectdist)

    logocos = cosineSimilarity(obj1["logo"], obj2["logo"])
    logodist = distance(obj1["logo"], obj2["logo"])
    print("logo values", logocos, logodist)

    paletteDist = (
        1
        - paletteDistance(obj1["color"]["palette"], obj2["color"]["palette"])
        / maxColourDistance
    )
    domColorDist = (
        1
        - colorDistance(obj1["color"]["dominant"], obj2["color"]["dominant"])
        / maxColourDistance
    )
    print("color values", paletteDist, domColorDist)

    if sum(obj1["logo"]) == 0 or sum(obj2["logo"]) == 0:
        finalValue = (
            labelcos * 0.33 + objectcos * 0.33 + (paletteDist + domColorDist) / 2 * 0.33
        )
    else:
        finalValue = (
            labelcos * 0.3
            + objectcos * 0.25
            + (paletteDist + domColorDist) / 2 * 0.25
            + logocos * 0.2
        )
    print("final:", finalValue, "\n")


def cosineSimilarity(v1, v2):
    if sum(v1) == 0 or sum(v2) == 0:
        return 0
    v1, v2 = np.array(v1), np.array(v2)  # remove
    dot_product = np.dot(v1, v2)
    norm_product = np.linalg.norm(v1) * np.linalg.norm(v2)

    return dot_product / norm_product


def distance(v1, v2):
    v1, v2 = np.array(v1), np.array(v2)  # remove
    return np.linalg.norm(v1 - v2)


